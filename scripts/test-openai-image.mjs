import { mkdir, writeFile } from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import {
  DEFAULT_IMAGE_MODEL,
  formatOpenAIImageDiagnostic,
  generateOpenAIImageWithDiagnostics,
  getRequestedImageModel,
  normalizeImageQuality,
} from './openai-image-utils.mjs';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const rootDir = path.resolve(__dirname, '..');
const outputPath = path.join(rootDir, 'apps', 'web', 'public', 'demo-assets', 'test-openai-image.png');

const apiKey = process.env.OPENAI_API_KEY;
const requestedModel = getRequestedImageModel();
const quality = normalizeImageQuality(process.env.OPENAI_IMAGE_QUALITY || process.env.OPENAI_IMAGE_TEST_QUALITY || 'low');
const printedDiagnostics = new Set();

function printDiagnosticOnce(diagnostic, nextModel = '') {
  const key = `${diagnostic.category}:${diagnostic.model}:${diagnostic.httpStatus || diagnostic.errorName || ''}:${diagnostic.openaiCode || diagnostic.errorMessage || ''}`;
  if (printedDiagnostics.has(key)) return;
  printedDiagnostics.add(key);
  console.error(formatOpenAIImageDiagnostic(diagnostic, nextModel));
}

async function main() {
  const result = await generateOpenAIImageWithDiagnostics({
    apiKey,
    requestedModel,
    prompt:
      'Create a simple original orange-red cinematic abstract test image for a fictional short-drama demo website. No text, no logo, no real people.',
    size: '1024x1024',
    quality,
    dramaId: 'test-openai-image',
    kind: 'test',
    allowModelFallback: requestedModel === DEFAULT_IMAGE_MODEL,
    onAttemptFailure: printDiagnosticOnce,
  });

  await mkdir(path.dirname(outputPath), { recursive: true });
  await writeFile(outputPath, result.buffer);
  console.log('OpenAI image generation OK');
  console.log(`model: ${result.model}`);
  console.log(`quality: ${quality}`);
  console.log(`output: ${path.relative(rootDir, outputPath)}`);
}

main().catch((error) => {
  const diagnostics = Array.isArray(error?.diagnostics) ? error.diagnostics : [];
  if (diagnostics.length) {
    diagnostics.forEach((diagnostic) => printDiagnosticOnce(diagnostic));
  } else {
    console.error('[chengying] OpenAI image generation diagnostic');
    console.error(`  error.name: ${error?.name || 'Error'}`);
    console.error(`  error.message: ${error?.message || String(error)}`);
  }
  console.error('OpenAI image generation failed. No local fallback image was generated by this test command.');
  process.exitCode = 1;
});
